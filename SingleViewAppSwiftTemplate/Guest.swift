import Foundation

class Guest {
    let type: GuestType
    let firstName: String
    let lastName: String
    let birthday: String?
    
    init(type: GuestType, firstName: String, lastName: String, birthday: String?) {
        self.type = type
        self.firstName = firstName
        self.lastName = lastName
        self.birthday = birthday
    }
}











































